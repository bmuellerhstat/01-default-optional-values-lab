def student_school(name,school="HSTAT")
    "#{name.capitalize} is a student at #{school}."
end

def student_interests(name,*hobbies)
    "#{name.capitalize} is interested in #{hobbies}."
end

def student_interests_proper(name,a,b,c)
    "#{name.capitalize} is interested in #{a}, #{b}, and #{c}."
end