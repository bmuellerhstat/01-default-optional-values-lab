def student_school(name, school="HSTAT")
    "#{name.capitalize} is a student at #{school}."
end


def student_interests (name,*interest)
"#{name.capitalize} is interested in #{interest}."
end
def student_interests_proper(name, interests1, interests2, interests3)
    "#{name.capitalize} is interested in #{interests1}, #{interests2}, and #{interests3}."
end